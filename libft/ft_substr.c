#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*des;

	if (!s)
		return (NULL);
	if (start >= ft_strlen(s))
		return (ft_strdup(""));
	if (len > ft_strlen(s + start))
		len = ft_strlen(s + start);
	des = (char *)malloc(len + 1);
	if (!des)
		return (NULL);
	ft_strlcpy(des, s + start, len + 1);
	return (des);
}
